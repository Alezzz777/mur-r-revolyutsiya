  function getLang() {
    var saved = localStorage.getItem('purr_lang');
    var available = Object.keys(I18N);
    if (saved && available.indexOf(saved) !== -1) return saved;
    var browser = ((navigator.language || navigator.userLanguage || '')).slice(0, 2).toLowerCase();
    if (browser && available.indexOf(browser) !== -1) return browser;
    return (document.body.dataset && document.body.dataset.defaultLang) || 'ru';
  }

  function applyI18n(lang) {
    var t = I18N[lang] || I18N.ru;
    localStorage.setItem('purr_lang', lang);
    document.documentElement.lang = lang;

    // RTL support for Arabic
    document.body.style.direction = lang === 'ar' ? 'rtl' : '';
    document.body.style.textAlign = lang === 'ar' ? 'right' : '';

    // Apply data-i18n elements
    var els = document.querySelectorAll('[data-i18n]');
    for (var i = 0; i < els.length; i++) {
      var key = els[i].getAttribute('data-i18n');
      if (t[key] !== undefined) els[i].textContent = t[key];
    }

    // Special: counter with embedded span
    var counterEl = document.querySelector('.counter');
    var cntEl = document.getElementById('cnt');
    if (counterEl && cntEl && t.counterTemplate) {
      var val = cntEl.textContent;
      counterEl.innerHTML = t.counterTemplate.replace('{n}', '<span id="cnt">' + val + '</span>');
    }

    // Update lang hint
    var hintEl = document.getElementById('langHint');
    if (hintEl && t.langHint) hintEl.textContent = t.langHint;

    // Update active state on flags
    var links = document.querySelectorAll('.lang-bar a[data-lang]');
    for (var j = 0; j < links.length; j++) {
      links[j].classList.toggle('active', links[j].getAttribute('data-lang') === lang);
    }
  }

  function renderLangBar() {
    // Remove old lang-switch if present
    var old = document.querySelector('.lang-switch');
    if (old) old.parentNode.removeChild(old);

    var bar = document.createElement('div');
    bar.className = 'lang-bar';
    var flagsHtml = LANGS.map(function (l) {
      return '<a href="#" data-lang="' + l.code + '" title="' + l.code.toUpperCase() + '">' + l.flag + '</a>';
    }).join('');
    bar.innerHTML = '<div class="lang-flags">' + flagsHtml + '</div><span class="lang-hint" id="langHint"></span>';

    // Insert after .paw-trail / #bgParticles, before first meaningful content
    var insertBefore = null;
    var children = document.body.children;
    for (var i = 0; i < children.length; i++) {
      var cls = children[i].className || '';
      var id = children[i].id || '';
      if (cls !== 'paw-trail' && id !== 'bgParticles') {
        insertBefore = children[i];
        break;
      }
    }
    if (insertBefore) {
      document.body.insertBefore(bar, insertBefore);
    } else {
      document.body.appendChild(bar);
    }

    bar.addEventListener('click', function (e) {
      var a = e.target.closest ? e.target.closest('a[data-lang]') : null;
      if (!a && e.target.getAttribute) a = e.target.getAttribute('data-lang') ? e.target : null;
      if (a) {
        e.preventDefault();
        applyI18n(a.getAttribute('data-lang'));
      }
    });
  }

  function init() {
    injectStyles();
    renderLangBar();
    applyI18n(getLang());
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }

